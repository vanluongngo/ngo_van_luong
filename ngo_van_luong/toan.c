////Chức năng: Cộng 2 số nguyên
//Input:
//	int a: Số nguyên thứ nhất
//	int b: Số nguyên thứ hai
//Output:
//	int: Kết quả của phép tính//

int cong(int a, int b)
{
	int tong = 0;
	tong = a + b;
	return tong;
}
